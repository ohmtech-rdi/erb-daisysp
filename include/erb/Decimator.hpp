/*
==============================================================================
Name: ctor
==============================================================================
*/

Decimator::Decimator ()
{
   _impl.Init ();
}


/*
==============================================================================
Name: operator ()
==============================================================================
*/

float Decimator::operator () (float x)
{
   return _impl.Process (x);
}



/*
==============================================================================
Name: operator ()
==============================================================================
*/

template <size_t size>
std::array <float, size>   Decimator::operator () (std::array <float, size> in)
{
   std::array <float, size> ret;
   for (size_t i = 0 ; i < size ; ++i)
   {
      ret [i] = _impl.Process (in [i]);
   }

   return ret;
}



