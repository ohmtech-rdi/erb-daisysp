/*\\\ PUBLIC \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name: ctor
==============================================================================
*/

Bitcrush::Bitcrush (size_t sample_rate)
{
   _impl.Init (float (sample_rate));
}


/*
==============================================================================
Name: operator ()
==============================================================================
*/

float Bitcrush::operator () (float x)
{
   return _impl.Process (x);
}



/*
==============================================================================
Name: operator ()
==============================================================================
*/

template <size_t size>
std::array <float, size>   Bitcrush::operator () (std::array <float, size> in)
{
   std::array <float, size> ret;
   for (size_t i = 0 ; i < size ; ++i)
   {
      ret [i] = _impl.Process (in [i]);
   }

   return ret;
}



/*\\\ INTERNAL \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name: impl_set_bit_depth
==============================================================================
*/

void  Bitcrush::impl_set_bit_depth (int depth)
{
   _impl.SetBitDepth (depth);
}



/*
==============================================================================
Name: impl_set_crush_rate
==============================================================================
*/

void  Bitcrush::impl_set_crush_rate (float freq)
{
   _impl.SetCrushRate (freq);
}



/*
==============================================================================
Name: impl_set_crush_rate
==============================================================================
*/

void  Bitcrush::impl_set_crush_rate (Frequency freq)
{
   _impl.SetCrushRate (freq.value);
}



