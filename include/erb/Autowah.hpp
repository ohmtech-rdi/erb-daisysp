/*\\\ PUBLIC \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name: ctor
==============================================================================
*/

Autowah::Autowah (size_t sample_rate)
{
   _impl.Init (float (sample_rate));
}


/*
==============================================================================
Name: operator ()
==============================================================================
*/

float Autowah::operator () (float x)
{
   return _impl.Process (x);
}



/*
==============================================================================
Name: operator ()
==============================================================================
*/

template <size_t size>
std::array <float, size>   Autowah::operator () (std::array <float, size> in)
{
   std::array <float, size> ret;
   for (size_t i = 0 ; i < size ; ++i)
   {
      ret [i] = _impl.Process (in [i]);
   }

   return ret;
}



/*\\\ INTERNAL \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name: impl_set_wah
==============================================================================
*/

void  Autowah::impl_set_wah (float wah)
{
   _impl.SetWah (wah);
}



/*
==============================================================================
Name: impl_set_dry_wet
==============================================================================
*/

void  Autowah::impl_set_dry_wet (float dry_wet)
{
   // normalize
   _impl.SetDryWet (dry_wet * 0.01f);
}



/*
==============================================================================
Name: impl_set_level
==============================================================================
*/

void  Autowah::impl_set_level (float level)
{
   _impl.SetLevel (level);
}



